#include "nbytes.h"



//=======================================================================================
//      Bytes
//=======================================================================================
NBytes::NBytes( GBytes *raw )
    : _p( raw, g_bytes_unref )
{}
//=======================================================================================
const void *NBytes::data() const
{
    return g_bytes_get_data( _p.get(), nullptr );
}
//=======================================================================================
size_t NBytes::size() const
{
    return g_bytes_get_size( _p.get() );
}
