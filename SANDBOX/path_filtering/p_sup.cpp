void norm( VPointD::Vector* v )
{
    auto xmin_it = std::min_element( v->begin(), v->end(), [](VPointD& p1, VPointD&p2)
    {
        return p1.x() < p2.x();
    });
    auto xmin = xmin_it->x();
    vdeb << xmin;
    for ( auto& p: *v )
    {
        p = {p.x() - xmin, p.y()};
    }

    auto ymin_it = std::min_element( v->begin(), v->end(), [](VPointD& p1, VPointD&p2)
    {
        return p1.y() < p2.y();
    });
    auto ymin = ymin_it->y();
    vdeb << ymin << (ymin_it - v->begin());
    for ( auto& p: *v )
    {
        p = {p.x(), p.y() - ymin};
    }
}

VPointD::Vector p_sup::pnts()
{
    VPointD::Vector res;
    QString alls = points2;
    auto lines = alls.split( "\n" );
    lines.removeAll( QString() );

    for( auto line: lines )
    {
        auto nums = line.split(" ");
        nums.removeAll( QString() );
        if ( nums.count() == 0 ) continue;

        Q_ASSERT( nums.count() == 3 );

        if ( nums[1].toDouble() < 60 )
            vdeb << "HERE!" << line;
        res.push_back( {nums[0].toDouble(), nums[1].toDouble()} );
    }
    norm( &res );
    return res;
}

