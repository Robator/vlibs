#include "vwatchdog.h"


//=======================================================================================
void VWatchDog::relax()
{
    _cur_times_count = _times_count;
}
//=======================================================================================
void VWatchDog::_on_timeout( int cnt )
{
    _cur_times_count -= cnt;

    if ( _cur_times_count <= 0 )
    {
        relax();
        expired();
    }
}
